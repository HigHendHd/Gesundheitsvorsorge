public class Uebung {
    private String uebung;
    private String uename;
    private String saetze;
    private String wiederholung;
    private String muskelgruppe;
    private String uenr;

    public String getMuskelgruppe() {
        return muskelgruppe;
    }

    public void setMuskelgruppe(String muskelgruppe) {
        this.muskelgruppe = muskelgruppe;
    }

    public String getSaetze() {
        return saetze;
    }

    public void setSaetze(String saetze) {
        this.saetze = saetze;
    }

    public String getUebung() {
        return uebung;
    }

    public void setUebung(String uebung) {
        this.uebung = uebung;
    }

    public String getUename() {
        return uename;
    }

    public void setUename(String uename) {
        this.uename = uename;
    }

    public String getUenr() {
        return uenr;
    }

    public void setUenr(String uenr) {
        this.uenr = uenr;
    }

    public String getWiederholung() {
        return wiederholung;
    }

    public void setWiederholung(String wiederholung) {
        this.wiederholung = wiederholung;
    }
}
